public class Exercise07 {
    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5};            //定义数组
        //使用for循环遍历数组元素
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
